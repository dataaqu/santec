/* ── applyLang ── */
window.applyLang = function (lang) {
  if (!window.I18N[lang]) lang = 'ka';
  var dict = window.I18N[lang];
  document.documentElement.lang = lang;

  document.querySelectorAll('[data-i18n]').forEach(function (el) {
    var k = el.getAttribute('data-i18n');
    if (dict[k] != null) {
      /* Use innerHTML when the value contains markup, textContent otherwise */
      if (dict[k].indexOf('<') !== -1) {
        el.innerHTML = dict[k];
      } else {
        el.textContent = dict[k];
      }
    }
  });

  document.querySelectorAll('[data-lang]').forEach(function (b) {
    var active = b.getAttribute('data-lang') === lang;
    b.setAttribute('aria-pressed', String(active));
    b.classList.toggle('bg-ink-800', active);
    b.classList.toggle('text-ink-50', active);
    b.classList.toggle('text-ink-400', !active);
  });

  document.dispatchEvent(new CustomEvent('langchange', { detail: { lang: lang } }));
};

/* ── Init on DOMContentLoaded ── */
if (typeof document !== 'undefined' && document.addEventListener) {
  document.addEventListener('DOMContentLoaded', function () {
    var saved = localStorage.getItem('lang');
    window.applyLang(saved || 'ka');
    document.querySelectorAll('[data-lang]').forEach(function (b) {
      b.addEventListener('click', function () {
        var lang = b.getAttribute('data-lang');
        localStorage.setItem('lang', lang);
        window.applyLang(lang);
      });
    });
  });
}
